
public class August17 {

	public static void main(String[] args) {
		
		byte val1 = 127; // 8 bit integer limit
		
		System.out.println("Value of val1 = "+val1);
		
		short val2 = 32767; // 16 bit integer limit
		
		System.out.println("Value of val2 = "+val2);
		
		int val3 = 2147483647; // 32 bit integer limit
		
		System.out.println("Value of val3 = "+val3);
		
		float val4 = 3.49879f; // use "f" after number in eclipse
		
		System.out.println("Value of val4 = "+val4);
		
		double val5 = 57.43267; // double storing capacity of float
		
		System.out.println("Value of val5 = "+val5);
		
		boolean val6 = true; // true or false
		
		System.out.println("Value of val6 = "+val6);
		
		char val7 = 101; // 101 is ASCII for e
		
		System.out.println("Value of val7 = "+val7);
		
		String val8 = "this is a string";
		
		System.out.println("Value of val8      = "+val8);
		

	}

}
